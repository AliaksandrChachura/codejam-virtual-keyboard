const textArea = new Keyboard(true, false);
document.body.innerHTML = textArea.createTextArea();
textArea.createKeyboard();
const keyBoard = document.getElementById('keyboard');

const input = document.getElementById('result');
input.focus();
keyBoard.addEventListener('click', (event) => {
  if (event.target.id === 'CapsLock') {
    keyBoard.innerHTML = '';
    textArea.changeCase();
    textArea.createKeyboard();
  }
});

keyBoard.addEventListener('mousedown', (event) => {
  input.focus();
  const {target} = event;
  const className = target.id;
  const el = document.getElementById(className);
  el.classList.add('click');
  if (className !== 'Tab' && className !== 'CapsLock' && className !== 'ShiftLeft' && className !== 'ShiftRight' && className !== 'ControlLeft' && className !== 'ControlRight' && className !== 'metaKey' && className !== 'Backspace' && className !== 'Delete' && className !== 'Enter' && className !== 'Space' && className !== 'AltLeft' && className !== 'AltRight' && className !== 'ArrowLeft' && className !== 'ArrowUp' && className !== 'ArrowDown' && className !== 'ArrowRight' && className !== 'Win' && className !== 'keyboard') {
    input.value += el.innerHTML;
  } else if (className === 'Tab') {
    input.value += '\t';
  } else if (className === 'Space') {
    input.value += ' ';
  } else if (className === 'ShiftLeft' || className === 'ShiftRight') {
    keyBoard.innerHTML = '';
    textArea.changeCase();
    textArea.createKeyboard();
  } else if (className === 'Enter') {
    input.value += '\n';
  }
});

keyBoard.addEventListener('mouseup', (event) => {
  input.focus();
  const {target} = event;
  const className = target.id;
  const el = document.getElementById(className);
  el.classList.remove('click');
  if (className === 'ShiftLeft' || className === 'ShiftRight') {
    keyBoard.innerHTML = '';
    textArea.changeCase();
    textArea.createKeyboard();
  }
});

keyBoard.addEventListener('mouseout', (event) => {
  const {target} = event;
  const className = target.id;
  const el = document.getElementById(className);
  el.classList.remove('click');
});

document.addEventListener('keydown', (event) => {
  input.focus();
  const className = event.code;

  if (event.code === 'AltLeft' || (event.ShiftLeft || event.metaKey)) {
    keyBoard.innerHTML = '';
    textArea.changeLanguage();
    localStorage.setItem(textArea.Language, textArea.Language);
    textArea.createKeyboard();
  } else if (event.code === 'ShiftLeft' || event.code === 'ShiftRight') {
    keyBoard.innerHTML = '';
    textArea.changeCase();
    textArea.createKeyboard();
  }
  const el = document.getElementById(className);
  el.classList.add('click');
});

document.addEventListener('keyup', (event) => {
  input.focus();
  const className = event.code;
  const el = document.getElementById(className);
  if (className === 'CapsLock') {
    keyBoard.innerHTML = '';
    textArea.changeCase();
    textArea.createKeyboard();
    el.classList.toggle('clickCaps');
  } else if (event.code === 'ShiftLeft' || event.code === 'ShiftRight') {
    keyBoard.innerHTML = '';
    textArea.changeCase();
    textArea.createKeyboard();
  }
  el.classList.remove('click');
});
